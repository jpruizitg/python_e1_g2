# operadores in y not in

print('Ej.1 - uso de in y not in')
my_list = [0, 3, 12, 8, 2]

print(5 in my_list)
print(5 not in my_list)
print(12 in my_list)

print('Ej.2 - uso de in')
wars = ['primera', 'segunda', 'tercera', 'persico']

resultado = 'tercera' in wars
print(resultado)

print()

resultado2 = 'cuarta' in wars
print(resultado2)
